package javaproject;

public class Break_continue {
	public static void main(String[] args)
	{
		for(int i=0;i<=10;i++)
		{
			if(i==5) 
			{
				break;   //break in iterative loop
			}
			System.out.println("hi"+i);
			
		}
		
		
		
		System.out.println(";;;;;;;;;;;;;;;;;;;;;;;;;;");
			
//		continue
		
		for(int i=0;i<=10;i++)
		{
			if(i==5) 
			{
				continue;   //break in iterative loop
			}
			System.out.println("hi"+i);
			
		}
		
		
		
	}
}
